int	ft_isalnum(int c)
{
	if (('A' <= c && 'Z' >= c) || ('a' <= c && 'z' >= c) || ('0' <= c
			&& '9' >= c))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
